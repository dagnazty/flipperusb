function updateConnectionStatus(status) {
    elements.statusIndicator.className = 'status-dot ' + status;
    elements.statusText.className = 'status-text ' + status;

    const titles = {
        connected: 'Connected to Flipper Zero',
        disconnected: 'Disconnected',
        connecting: 'Connecting...'
    };
    elements.statusIndicator.title = titles[status] || status;
    elements.statusText.textContent = status === 'connected'
        ? 'Connected'
        : status === 'connecting'
            ? 'Connecting'
            : 'Disconnected';
    elements.supportText.textContent = !serialSupported
        ? 'Open this site in desktop Chrome or Edge'
        : status === 'connected'
            ? 'USB serial session active'
            : 'Desktop Chrome or Edge with Web Serial';

    // Update page title
    document.title = status === 'connected'
        ? 'FlipperUSB - Connected'
        : 'FlipperUSB - Web File Manager';

    updateWorkspaceControls();

    switch (status) {
        case 'connected':
            toast.success('Connected to Flipper Zero');
            debugLog.success('Connected to Flipper Zero');
            break;
        case 'disconnected':
            debugLog.warning('Disconnected from Flipper Zero');
            break;
        case 'connecting':
            debugLog.info('Attempting to connect to Flipper Zero...');
            break;
    }
}

