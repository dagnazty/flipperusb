function isConnected() {
    return Boolean(flipper.port);
}

function getDirectoryKind(path = currentPath) {
    if (path.includes('/badusb')) return 'badusb';
    if (path.includes('/nfc')) return 'nfc';
    if (path.includes('/subghz')) return 'subghz';
    if (path.includes('/infrared')) return 'infrared';
    return 'generic';
}

function getTemplateOptions(path = currentPath) {
    return TEMPLATE_LIBRARY[getDirectoryKind(path)] || TEMPLATE_LIBRARY.generic;
}

function getDraftKey(path) {
    return `${DRAFT_STORAGE_PREFIX}${path}`;
}

function getDraftContent(path) {
    if (!path) return null;

    try {
        return localStorage.getItem(getDraftKey(path));
    } catch (error) {
        debugLog.warning(`Draft read failed: ${error.message}`);
        return null;
    }
}

function saveDraftContent(path, content) {
    if (!path) return;

    try {
        localStorage.setItem(getDraftKey(path), content);
    } catch (error) {
        debugLog.warning(`Draft save failed: ${error.message}`);
    }
}

function clearDraftContent(path) {
    if (!path) return;

    try {
        localStorage.removeItem(getDraftKey(path));
    } catch (error) {
        debugLog.warning(`Draft clear failed: ${error.message}`);
    }
}

function syncDraftState() {
    if (!currentFile) return;

    if (elements.editor.value === lastSavedContent) {
        clearDraftContent(currentFile);
    } else {
        saveDraftContent(currentFile, elements.editor.value);
    }
}

function buildPath(basePath, name) {
    return basePath + (basePath.endsWith('/') ? '' : '/') + name;
}

