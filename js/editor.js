function getPathBreadcrumbs(path) {
    const parts = path.split('/').filter(p => p);
    let breadcrumbs = '';
    let fullPath = '';

    breadcrumbs += `<span class="breadcrumb-item" data-path="/">root</span>`;
    parts.forEach(part => {
        fullPath += '/' + part;
        breadcrumbs += ` / <span class="breadcrumb-item" data-path="${fullPath}">${part}</span>`;
    });

    return breadcrumbs;
}

