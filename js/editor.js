function renderFileItems(items, appendMode = false) {
    if (items.length === 0 && !appendMode) {
        elements.fileList.innerHTML = `
            <div class="empty-dir">
                <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="1.5">
                    <path d="M22 19a2 2 0 0 1-2 2H4a2 2 0 0 1-2-2V5a2 2 0 0 1 2-2h5l2 3h9a2 2 0 0 1 2 2z"/>
                </svg>
                <p>This directory is empty</p>
                <button class="flipper-button small secondary" onclick="document.getElementById('newFileBtn').click()">
                    Create a file
                </button>
            </div>
        `;
        return;
    }

    // If no items in append mode (root), just return - quick access already shown
    if (items.length === 0) return;

    // Sort: directories first, then alphabetically
    const sortedItems = items.sort((a, b) => {
        if (a.type === b.type) return a.name.localeCompare(b.name);
        return a.type === 'directory' ? -1 : 1;
    });

    sortedItems.forEach(item => {
        const div = document.createElement('div');
        div.className = 'file-item';
        if (currentFile === item.path) {
            div.classList.add('active');
        }

        const icon = getFileIcon(item.name, item.type === 'directory');
        const iconClass = item.type === 'directory' ? 'icon folder' : 'icon';

        div.innerHTML = `
            <span class="${iconClass}">${icon}</span>
            <span class="name">${item.name}</span>
            ${item.type === 'file' ? `
                <span class="size">${item.size || ''}</span>
                <button class="download-btn" title="Download">
                    <svg viewBox="0 0 24 24" width="16" height="16" fill="none" stroke="currentColor" stroke-width="2">
                        <path d="M21 15v4a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2v-4"/>
                        <polyline points="7 10 12 15 17 10"/>
                        <line x1="12" y1="15" x2="12" y2="3"/>
                    </svg>
                </button>
                <button class="delete-btn" title="Delete">
                    <svg viewBox="0 0 24 24" width="16" height="16" fill="none" stroke="currentColor" stroke-width="2">
                        <polyline points="3 6 5 6 21 6"/>
                        <path d="M19 6v14a2 2 0 0 1-2 2H7a2 2 0 0 1-2-2V6m3 0V4a2 2 0 0 1 2-2h4a2 2 0 0 1 2 2v2"/>
                    </svg>
                </button>
            ` : ''}
        `;

        if (item.type === 'directory') {
            div.addEventListener('click', async () => {
                currentPath = item.path;
                await loadFileList();
            });
        } else {
            div.addEventListener('click', (e) => {
                // Don't trigger if clicking action buttons
                if (!e.target.closest('.download-btn') && !e.target.closest('.delete-btn')) {
                    loadFile(item.path);
                }
            });

            // Download button
            const downloadBtn = div.querySelector('.download-btn');
            downloadBtn.addEventListener('click', async (e) => {
                e.stopPropagation();
                await downloadFile(item.path, item.name);
            });

            // Delete button
            const deleteBtn = div.querySelector('.delete-btn');
            deleteBtn.addEventListener('click', async (e) => {
                e.stopPropagation();
                showConfirmModal(
                    'Delete File',
                    `Are you sure you want to delete "${item.name}"?`,
                    async () => {
                        if (await flipper.deleteFile(item.path)) {
                            toast.success(`Deleted: ${item.name}`);
                            clearDraftContent(item.path);
                            if (currentFile === item.path) {
                                currentFile = null;
                                lastSavedContent = '';
                                elements.editor.value = '';
                                elements.editor.disabled = true;
                                elements.saveBtn.disabled = true;
                                elements.downloadBtn.disabled = true;
                                setCurrentFileLabel('Choose a file to begin');
                                setEditorWelcome('connected');
                            }
                            await loadFileList();
                        } else {
                            toast.error('Failed to delete file');
                        }
                    },
                    null,
                    {
                        confirmLabel: 'Delete',
                        confirmClass: 'danger'
                    }
                );
            });
        }

        elements.fileList.appendChild(div);
    });
}

