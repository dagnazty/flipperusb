function showRestoreDraftPrompt(path, filename, deviceContent) {
    const draftContent = getDraftContent(path);
    if (draftContent === null || draftContent === deviceContent) {
        return;
    }

    showConfirmModal(
        'Local Draft Found',
        `A newer unsaved draft for "${filename}" was found in this browser. Restore it?`,
        () => {
            elements.editor.value = draftContent;
            hasUnsavedChanges = true;
            setCurrentFileLabel(filename, true);
            toast.info(`Restored draft for ${filename}`);
        },
        () => {
            clearDraftContent(path);
            toast.info(`Discarded saved draft for ${filename}`);
        },
        {
            confirmLabel: 'Restore Draft',
            cancelLabel: 'Discard Draft',
            confirmClass: 'primary'
        }
    );
}

function openCreateFolderModal() {
    showFormModal({
        title: 'Create Folder',
        message: `Create a new folder inside ${currentPath}.`,
        submitLabel: 'Create Folder',
        fields: [
            {
                name: 'folderName',
                label: 'Folder Name',
                placeholder: 'new-folder',
                required: true,
                autofocus: true,
                hint: 'Use a simple folder name without slashes.'
            }
        ],
        onSubmit: async (values) => {
            const folderName = values.folderName.trim();

            if (!folderName || folderName.includes('/')) {
                toast.error('Folder names cannot be empty or contain slashes.');
                return false;
            }

            const path = buildPath(currentPath, folderName);
            const success = await flipper.createDirectory(path);
            if (!success) {
                toast.error(`Failed to create folder: ${folderName}`);
                return false;
            }

            toast.success(`Created folder: ${folderName}`);
            await loadFileList();
            return true;
        }
    });
}

function openCreateFileModal() {
    const templates = getTemplateOptions();
    const suggestedExtension = templates[0]?.extension || '';

    showFormModal({
        title: 'Create File',
        message: `Create a new file inside ${currentPath}.`,
        submitLabel: 'Create File',
        fields: [
            {
                name: 'filename',
                label: 'File Name',
                placeholder: suggestedExtension ? `my-file${suggestedExtension}` : 'my-file.txt',
                required: true,
                autofocus: true,
                hint: suggestedExtension
                    ? `You can omit ${suggestedExtension} and it will be added for you.`
                    : 'Add an extension if you want one.'
            },
            {
                name: 'template',
                label: 'Starter Template',
                type: 'radio',
                value: templates[0]?.value,
                options: templates.map((template) => ({
                    value: template.value,
                    label: template.label,
                    description: template.description
                }))
            }
        ],
        onSubmit: async (values) => {
            const template = templates.find((item) => item.value === values.template) || templates[0];
            const rawFilename = values.filename.trim();

            if (!rawFilename || rawFilename.includes('/')) {
                toast.error('File names cannot be empty or contain slashes.');
                return false;
            }

            const filename = template.extension && !rawFilename.endsWith(template.extension)
                ? `${rawFilename}${template.extension}`
                : rawFilename;
            const path = buildPath(currentPath, filename);

            const success = await flipper.writeFile(path, template.content);
            if (!success) {
                toast.error(`Failed to create file: ${filename}`);
                return false;
            }

            clearDraftContent(path);
            toast.success(`Created: ${filename}`);
            await new Promise((resolve) => setTimeout(resolve, 300));
            await loadFileList();
            await doLoadFile(path);
            return true;
        }
    });
}

