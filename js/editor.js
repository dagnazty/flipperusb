elements.connectBtn.addEventListener('click', async () => {
    if (!serialSupported) {
        toast.error('Web Serial is not available in this browser.', {
            title: 'Unsupported Browser'
        });
        return;
    }

    updateConnectionStatus('connecting');
    elements.connectBtn.disabled = true;

    try {
        if (await flipper.connect()) {
            elements.connectBtn.style.display = 'none';
            elements.disconnectBtn.style.display = 'inline-flex';
            updateConnectionStatus('connected');
            setCurrentFileLabel('Choose a file to begin');
            setEditorWelcome('connected');
            await loadFileList();
        } else {
            throw new Error('Connection failed');
        }
    } catch (error) {
        toast.error('Connection failed: ' + error.message, {
            title: 'Connection Error'
        });
        debugLog.error('Connection error: ' + error.message);
        updateConnectionStatus('disconnected');
    } finally {
        elements.connectBtn.disabled = false;
    }
});

